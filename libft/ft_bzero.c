#include "libft.h"

void	*ft_bzero(void *str, size_t len)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)str;
	while (len > 0)
	{
		*tmp = 0;
		tmp++;
		len--;
	}
	return (str);
}
